<section>
    <header class="section-header"><h1>Présentation du logiciel</h1></header>
    <article>
        Ce logiciel permet de créer des rencontres de pétanque sur le principe de la mêlée en tenant compte des joueurs présents.
        On peut créer de 1 à 4 manches sur une journée, la composition des équipes est générée de façon aléatoire pour chaque manche.
    </article>

    <article>
        <header class="section-header"><h4>Configuration</h4></header>
        <div class="content">Ce menu est disponible en haut à droite (<i class="fa fa-cog"></i>). On peut y ajouter ou supprimer des joueurs, supprimer des days et des manches.</div>
    </article>

    <article>
        <header class="section-header"><h4>Choix des joueurs</h4></header>
        <div class="content">On sélectionne les joueurs présents avant chaque manche en utilisant le filtre des habitués si besoin.
            Le nombre de joueurs sélectionnés détermine automatiquement la composition des équipes.</div>
    </article>
    
    <article>
        <header class="section-header"><h4>Démarrage</h4></header>
        <div class="content">Ce menu permet d'activer la création d'une manche puis de créer les équipes et le tableau des rencontres.</div>
    </article>
    
